    private void jButtonLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLoginActionPerformed
    String numero_conta = jTextFieldNumConta.getText();
    String senha = jPasswordFieldSenha.getText();
    try (Connection conexao = Conexao.conectar()) {
            String sql = "SELECT * FROM contas WHERE numero = ? AND senha = ?";
            try (PreparedStatement stmt = conexao.prepareStatement(sql)) {
                stmt.setString(1, numero_conta);
                stmt.setString(2, senha);
                try (ResultSet resultado = stmt.executeQuery()) {
                    if (resultado.next()) {
                        JOptionPane.showMessageDialog(null, "Login realizado com sucesso!");
                        MenuLogado contaLogada = new MenuLogado();
                        contaLogada.setVisible(true);
            
            
        } else {
            JOptionPane.showMessageDialog(null, "Número da conta ou senha inválidos.");
        }
                }
            }
        } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Erro ao conectar ao banco de dados: " + e.getMessage());
        }
    }//GEN-LAST:event_jButtonLoginActionPerformed

